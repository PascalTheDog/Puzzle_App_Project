package com.example.david.puzzle_app_project.Graphics;

public class Vector3D
{
    public double x;
    public double y;
    public double z;

    public Vector3D()
    {
        x = 0;
        y = 0;
        z = 0;
    }

    public Vector3D(double _x, double _y, double _z)
    {
        x = _x;
        y = _y;
        z = _z;
    }
}
